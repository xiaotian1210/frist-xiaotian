package com.shareworx.ezfm.system.crop.service;

import com.shareworx.ezfm.system.crop.model.CropModel;

public interface ICropService {
	public static String ID = "cropService";
	/**
	 * 根据编码查询公司对象
	 * @param code
	 * @return
	 */
	CropModel queryForObject(String code);
	/**
	 * 根据编码查询公司主键ID
	 * @param code
	 * @return
	 */
	String queryForId(String code);
	
	/**
	 * 根据ID查询公司对象
	 * @param id
	 * @return
	 */
	CropModel queryForObjectById(String id);
}
