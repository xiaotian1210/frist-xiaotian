/**
 * 格式化字段：状态
 * 
 * @param {Object}
 *            value
 * @param {Object}
 *            row
 * @param {Object}
 *            index
 */
function formatWarnState(value, row, index) {
	switch (value) {
	case "0":
		return "待处理";
		break;
	case "1":
		return "已报修";
		break;
	case "2":
		return "已忽略";
		break;
	}
}
